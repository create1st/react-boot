package com.create.controller;

import org.junit.Before;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.test.context.ContextConfiguration;
import org.springframework.test.context.junit4.SpringJUnit4ClassRunner;
import org.springframework.test.context.web.WebAppConfiguration;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;
import org.springframework.web.context.WebApplicationContext;

import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.view;

@RunWith(SpringJUnit4ClassRunner.class)
@ContextConfiguration(classes = {ErrorController.class})
@WebAppConfiguration
public class ErrorControllerTest {
    private MockMvc mockMvc;

    @Autowired
    private WebApplicationContext webApplicationContext;

    @Before
    public void setUp() {
        mockMvc = MockMvcBuilders.webAppContextSetup(webApplicationContext).build();
    }

    @Test
    public void showInternalServerError() throws Exception {
        mockMvc.perform(get("/error/error"))
                .andExpect(status().isInternalServerError())
                .andExpect(view().name(ErrorController.INTERNAL_SERVER_ERROR_TEMPLATE));
    }

    @Test
    public void showNotFound() throws Exception {
        mockMvc.perform(get("/error/404"))
                .andExpect(status().isNotFound())
                .andExpect(view().name(ErrorController.NOT_FOUND_TEMPLATE));
    }
}
